package com.sourceit.java.basic.lesson26.exercise;

public class Person {
	 // Instance variables 
    private String name; 
    private int age; 
  
  // Constructor 
    public Person(String name, int age) 
    { 
        this.name = name; 
        this.age = age; 
    } 

  // Instance methods 
    public String getName() 
    { 
        return name; 
    } 

    public int getAge() 
    { 
        return age; 
    } 
}
